# Utility functions

def load_input(infile: str) -> Lines:
    return load_text(Path(infile).read_text())

def sample_case(idx: int = 0) -> Tuple[Lines, int]:
    text, expected = SAMPLE_CASES[idx]
    lines = load_text(text)
    return lines, expected

def load_text(text: str) -> Lines:
    return filter_blank_lines(text.split("\n"))

def filter_blank_lines(lines: Lines) -> Lines:
    return [line.strip() for line in lines if line.strip()]

# Solution

def solve2(lines: Lines) -> int:
    """Solve the problem."""
    device = Device()
    device.load_prog(lines)
    device.run()
    print()
    device.display()
    print()
    return "".join(device.pixels)

def solve(lines: Lines) -> int:
    """Solve the problem."""
    device = Device()
    device.load_prog(lines)
    device.run()

    if len(device.history) < 21:
        return [(5, device.history[5]), (6, device.history[6])]

    result = 0
    for cycles in (20, 60, 100, 140, 180, 220):
        print(f"after {cycles} cycles, register is {device.history[cycles]}")
        result += cycles * device.history[cycles]
    return result


